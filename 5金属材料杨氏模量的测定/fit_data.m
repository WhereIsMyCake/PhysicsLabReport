% =============================
% 弹性系数实验数据线性拟合
% 拟合方程：mg = k * Δs
% =============================

clc; clear; close all;

% -----------------------------
% 实验数据
% -----------------------------
m = [1 2 3 4 5 6 7 8];                    % kg
%s_avg = [4.52 3.86 3.38 2.85 2.27 1.66 1.09 0.62];  % cm
s_avg = [0.62 1.09 1.66 2.27 2.85 3.38 3.86 4.52]

% -----------------------------
% 单位换算
% -----------------------------
g = 9.8;                % m/s^2
ds = s_avg * 1e-2;      % cm -> m
mg = m * g;             % N

% -----------------------------
% 拟合：mg = k * ds + b
% -----------------------------
p = polyfit(ds, mg, 1);    % 一次线性拟合，返回 [k, b]
k_fit = p(1);              % 斜率 (弹性系数)
b_fit = p(2);              % 截距

% -----------------------------
% 计算拟合值和不确定度
% -----------------------------
mg_fit = polyval(p, ds);         % 拟合值
residuals = mg - mg_fit;         % 残差
n = length(ds);

% 斜率标准不确定度 (线性回归统计公式)
delta_k = sqrt(sum(residuals.^2) / (n - 2)) / sqrt(sum((ds - mean(ds)).^2));

% 三倍不确定度
Delta_k = 3 * delta_k;

% -----------------------------
% 输出结果
% -----------------------------
fprintf('平均拟合刚度系数  k̄ = %.4e N/m\n', k_fit);
fprintf('斜率标准不确定度 Δk = %.4e N/m\n', delta_k);
fprintf('三倍不确定度     3Δk = %.4e N/m\n', Delta_k);

% -----------------------------
% 绘图
% -----------------------------
figure;
plot(ds, mg, 'o', 'MarkerFaceColor', 'b'); hold on;
plot(ds, mg_fit, '-r', 'LineWidth', 1.5);
xlabel('Δs / m');
ylabel('mg / N');
title('线性拟合：mg = kΔs');
legend('实验数据','线性拟合');
grid on;
